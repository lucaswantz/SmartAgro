    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Aplica o validador
        // JComponent[] components = new JComponent[]{tfdDescricao};
        //VerificadorCampos verifier = new VerificadorCampos(components);

//        if (!verifier.validaCampos()) {
//            return;
//        }
        this.dao = new GenericDAO<>();

        if (rbtAudAtivada.isSelected()) {
            this.config.setAuditorialigada(true);
        } else {
            this.config.setAuditorialigada(false);
        }

        if (this.dao.atualizar(config)) {
            Mensagem.mostraInformacao("Sucesso", "Parâmetros atualizados com sucesso");
            FrmPrincipal.setParametros(config);

        } else {
            Mensagem.mostraErro("Problema", "Problema para atualizar parâmetros");
        }

        focus();

    }//GEN-LAST:event_btnSalvarActionPerformed

