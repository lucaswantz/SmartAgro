    public ArrayList<Contareceber> getCondicoesPagamentoVenda(Venda venda) {

        ArrayList<Contareceber> parcelas = new ArrayList();

        int parc = Integer.parseInt(cbmParcelas.getSelectedItem().toString());
        BigDecimal valor = venda.getValortotal().divide(BigDecimal.valueOf(parc), 2);

        Date data_venda = new java.util.Date();
        
        Calendar cal = Calendar.getInstance();
        cal.setTime(data_venda);
        
        // Data nao é obrigatoria
        try {
            int dia_do_vencimento = Integer.parseInt(tfdData.getText());
            cal.set(Calendar.DAY_OF_MONTH, dia_do_vencimento);
        } catch (Exception e) {
        }

        for (int i = 1; i <= parc; i++) {
            Contareceber c = new Contareceber();

            cal.add(Calendar.MONTH, 1);

            c.setParcela(i);
            c.setStatus(Contareceber.STATUS_PENDENTE);
            c.setValorpago(BigDecimal.ZERO);
            c.setValordevido(valor);
            c.setVencimento(cal.getTime());
            c.setVenda(venda);

            parcelas.add(c);
        }

        return parcelas;
    }
    
